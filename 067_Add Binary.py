'''
//////////////////////////////////////////////////////////////////////////////////////////////////////////

67. Add Binary

Given two binary strings a and b, return their sum as a binary string.

//////////////////////////////////////////////////////////////////////////////////////////////////////////

Example:

Input: a = "11", b = "1"
Output: "100"

Input: a = "1010", b = "1011"
Output: "10101"

//////////////////////////////////////////////////////////////////////////////////////////////////////////
'''


# string
class Solution:
    def addBinary(self, a: str, b: str) -> str:
        return bin(int(a, 2)+ int(b, 2))[2:]
           
                
            
    
if __name__ == '__main__':

    s = Solution()
    print(s.addBinary("1010", "1011"))     
        